package pertemuan2;

public class LatihanVariable {
	
	public static void main(String [] args) {
		/*
		 * Variable adalah wadah penyimpan nilai
		 * formatnya adalah
		 * [tipedata] [namaVariable] = [nilai];
		 * 
		 * Variable bersifat replaceable (mudah diganti)
		 * 
		 */
		
		String nama = "Udin";
		System.out.println("Nama saya " + nama);
		nama = "Endah";
		System.out.println("Nama saya " + nama);
	}

}
